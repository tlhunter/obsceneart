<h3>About ObsceneArt</h3>
<p>OA is a place where you can take all the silly things people type and save it in one place, for the rest of the world to laugh at! Whether it be work related emails, instant messages, letters sent from horny idiots on dating sites, or even something you found in the Bible, there is a place for it on ObsceneArt.</p>
<p>You can also use OA as a generic text / code storage website. Create an account to manage your posts, or don't make an account and submit the horrible code you find at work so that you can shame your co-workers anomymously!</p>
<p>OA claims no ownership of any texts uploaded by users. Use the <a href="<?=base_url()?>about/contact">contact</a> form to request a takedown (include the URLs).</p>
